// Package vanilla is Reflexion Health's Golang standard library.
// Many of the libraries are focused on supporting web services by providing
// additional support for sql, http, json, etc...
//
// Packages:
//   clock  - extensions to the "time" package that encourage timezones and allow time to be modified in tests
//   crypto - extensions to the "crypto" package to simplify common crypto operations
//   date   - extensions to the "time" package that deal explicitly with dates
//   expect - extensions to the "testing" package with simple assertions/expectations
//   httpx  - extensions to the "net/http" package with better Mux and Middleware
//   null   - utils for nullable types that support database/sql, encoding/gob, and encoding/json
//   semver - utils for Semantic Versioning
//   sql    - extenstions to the "database/sql" package to help with dynamic SQL
//   uuid   - utils for UUIDs (Universally Unique Identifiers)
//
package vanilla
